using System;

class Program
{
    static void Main()
    {
        Console.WriteLine("Hello, World!");
    }
}
 
 The  Main  method is the entry point of the program. It is the first method that is called when the program is executed. 
 The  Console.WriteLine  method writes the specified string to the console. 
 To compile and run the program, open a terminal and run the following commands: 
 csc main.cs